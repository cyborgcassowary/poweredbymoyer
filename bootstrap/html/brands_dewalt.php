      <h2>A Brief History</h2>
      <p>The original company was started in 1936 by Raymond E. DeWalt, the inventor of the radial arm saw. The company grew quickly and was reorganized in 1947, manufacturing radial arm saws and other stationary woodworking machines.<br />
        <br />
      American Machine &amp; Foundry bought the company in 1949 and sold it to Black &amp; Decker in 1960.</p>
      <p>Black &amp; Decker divested itself of the radial arm saw manufacturing branch in 1989, selling it to two executives. Radial arm saws that use the original DeWalt design can still be obtained from the Original Saw Co.</p>
      <p>In 1992, Black &amp; Decker started a major effort to rebrand its professional quality and high-end power tools to DeWalt. Currently, DeWalt manufactures and sells more than 200 different power hand-tools and 800 accessories.</p>
      <p>In 1994, DeWalt took over the German wood working power tool producer ELU. DeWalt increased their line of tools using ELU's technology. DeWalt is now a popular brand of tools for commercial contractors.</p>
      <p>In 2004, Black and Decker bought rival power tool manufacturer Porter-Cable and combined it with DeWalt in Jackson, Tennessee.</p>
      <p>DeWalt now produces a full line of compact and lithium ion cordless drills. The same battery used for the drill can also run one of the 40 other tools DeWalt produces, such as impact wrenches and circular saws.</p>
    



