      <h2>A brief hisorty of Porter-Cable</h2>
      <p>Porter-Cable is an American company that manufactures power tools. The company is known for introducing a number of noteworthy power tools, such as the first portable belt sander, helical-drive circular saw, and portable band saw. It is a subsidiary of Stanley Black &amp; Decker.</p>
      <p>Porter-Cable was founded in 1906 by R.E. Porter, G.G. Porter, and F.E. Cable, who, starting with a $2,300 investment, opened a jobbing machine and tool shop out of their garage in Syracuse, New York. In 1914, the company began to focus on power tools, starting with a line of power lathes. In 1917, the company bought a plant on North Salina Street in Syracuse.</p>
      <p>In 1926, Chief Engineer Art Emmons invented the portable electric belt sander, called the Take-About Sander, and the company began to develop a niche in portable electric power tools. In 1929, Emmons invented the helical-drive circular saw, a compact, lightweight design that is still the most widely used circular saw design produced today.</p>
      <p>In 1960, the company was sold to Rockwell International. Rockwell made numerous changes, including phasing out the Porter-Cable name, relocating the company's base of operations to Jackson, Tennessee, and creating a lower end of power tools to compete with Black &amp; Decker. These tools had numerous reliability problems and harmed the brand's image.</p>
      <p>In 1981, Pentair, Inc. acquired Rockwell's power tool group—consisting of Porter-Cable and Delta Machinery—and restored the Porter-Cable name. The company repositioned itself as a manufacturer of professional power tools and ended production of consumer-level tools. In 1989, the company introduced the first electric random orbital sander. Around this time, the company expanded into broader retail channels, including The Home Depot and Lowe's, greatly expanding its production in the process.</p>
      <p>In 1996, the Smithsonian Institution established a collection of materials from the company's 90-year history, the first such collection for a power tool company.<br />
        In 2000, Porter-Cable consolidated with sister company Delta Machinery, the latter moving its headquarters and distribution center from Pittsburgh, Pennsylvania to Jackson. The same year, Pentair acquired DeVilbiss Air Power Company and, in 2002, Porter-Cable expanded its line-up to include air compressors, air tools, generators, and pressure washers.</p>
      <p>In 2005, the Pentair Tools Group—comprising Porter-Cable, Delta Machinery, DeVilbiss Air Power, and others—was purchased by Black &amp; Decker, now Stanley Black &amp; Decker.</p>
