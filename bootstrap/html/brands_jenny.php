      <h2 class="headline">BRANDS - Jenny</h2>
      <p><img src="../images/logo_jenny.jpg" width="250" height="121" class="logo" /><br />
      Jenny has roots dating back to the late 1870’s and its subsequent incorporation in 1892 as Homestead Valve Manufacturing Company. Homestead Valve was located in Coraopolis, Pennsylvania where they designed, cast, machined, and tested a variety of ball and plug valves for the steel industry.</p>
      <p>In 1927, Jenny revolutionized cleaning worldwide with the invention of the steam cleaner. &quot;Steam Jenny&quot;, as it came to be called, facilitated the cleaning of dirty, greasy parts and equipment in an efficient and cost-effective manner, thereby reducing the amount of time and labor required.</p>
      <p>Jenny is credited for building the very first positive displacement triplex pump with an oil-bath crankcase for steam cleaners. Fabricated back around 1934, it was used for moving chemicals in plants where they needed an adjustable flow three plunger pump. Due to cost concerns, Homestead later switched to a less expensive reciprocating piston pump around the late 1930s. Triplex Plunger Pumps are still in use today, and are the backbone of every pressure washer currently manufactured.</p>
      <p>In the mid to late 1950’s, Jenny invented the first self service car wash system which was the size of an upright wall bookshelf. At the time however, service stations were doing most of the car washing for their customers and the system only ever had limited success.</p>
      <p>In 1992, a gentleman by the name of Peter Leiss purchased the Jenny division of Homestead Industries and relocated the plant to Somerset, Pennsylvania across the way from his privately owned Tool &amp; Die, Machining, Welding, Fabricating and ASME Code Air Compressor Tank Shop.</p>
      <p>In 1998, Davey Compressor Company was purchased and joined the Jenny product offering. Davey Compressor, an off-shoot of the Davey Expert Tree Company, was a leading innovator in the air compressor field. The company designed and built the world’s first, portable, air-cooled air compressor in the late 1920’s and has remained on the cutting edge of compressor technology.</p>
      <p>On November 20, 2003, Jenny purchased the Emglo line of Industrial Stationary Air Compressors and the entire Air Compressor Pump Manufacturing Facility from Black&amp;Decker/DeWalt prior to the relocation of their plant to Renosa, Mexico. Jenny and our affiliated companies had been supplying and manufacturing a large number of parts, components, and assemblies for Emglo for over 30 years.</p>
      <p>In keeping with our heritage of being a leading innovator and manufacturer of the finest available equipment, we manufacture a complete and extensive line of Hand Carry, Single and Two-Stage Wheeled Portable, Service Vehicle Compressor, Industrial Stationary, Base Plated Mounted, Climate Control and Fire Sprinkler Compressors.</p>
